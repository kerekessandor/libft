#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	while (*s1 != '\0' && *s1 != *s2)
	{
		s1++;
	}
	if (s1 == s2)
	{
		return ((char*)s1);
	}
	return (NULL);
}
